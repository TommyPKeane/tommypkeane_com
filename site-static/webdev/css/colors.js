let build_color_block_fnc = function hst_build_color_block_fnc(
      css_level_str,
      css_color_str,
      css_color_type_str,
   ) {
   let block_frag = new DocumentFragment();

   let block_div = document.createElement("div");
   block_div.className = "demo_color_block_container";

   let color_value_str = css_colors_obj[css_level_str][css_color_str][css_color_type_str];

   let color_block = document.createElement("div");
   color_block.className = "demo_color_block";
   color_block.id = ("cb_" + css_color_str);
   color_block.setAttribute(
      "title",
      (
         css_color_str + "\n"
         + css_color_type_str + ": " + color_value_str + "\n"
         + "CSS Level: " + css_level_str
      )
   );
   color_block.style.backgroundColor = css_color_str;

   let color_name_p = document.createElement("p");
   color_name_p.innerHTML = (css_color_str);

   let color_value_p = document.createElement("p");
   color_value_p.innerHTML = (color_value_str);

   let color_value_rgb_p = document.createElement("p");
   let red_int = parseInt(color_value_str.substring(1, 3), 16);
   let blu_int = parseInt(color_value_str.substring(3, 5), 16);
   let grn_int = parseInt(color_value_str.substring(5), 16);
   color_value_rgb_p.innerHTML = (
      "rgba("
      + red_int.toString().padStart(3, " ") + ", "
      + blu_int.toString().padStart(3, " ") + ", "
      + grn_int.toString().padStart(3, " ")
      + ");"
   );

   let color_level_p = document.createElement("p");
   color_level_p.innerHTML = ("CSS Level: " + css_level_str);

   block_div.appendChild(color_block);
   block_div.appendChild(color_name_p);
   block_div.appendChild(color_value_p);
   block_div.appendChild(color_value_rgb_p);
   block_div.appendChild(color_level_p);

   block_frag.appendChild(block_div);
   return (block_frag);
}


let build_all_blocks_fnc = function hst_build_all_blocks_fnc() {
   let css_levels = ["1", "2", "3", "4"];
   
   for (css_level_str of css_levels) {
      let article_body_elm = document.getElementById("id_lvl" + css_level_str + "_named");
      let css_color_type_str = "hex";

      for (css_color_str in css_colors_obj[css_level_str]) {
         let tmp_frag = build_color_block_fnc(
            css_level_str,
            css_color_str,
            css_color_type_str,
         )
         article_body_elm.appendChild(tmp_frag);   
      }
   }

   return;
}


document.addEventListener("DOMContentLoaded", build_all_blocks_fnc, false);
